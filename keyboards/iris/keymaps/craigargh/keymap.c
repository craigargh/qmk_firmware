bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LWR:
      if (record->event.pressed) {
        layer_on(_LWR);
        update_tri_layer(_LWR, _RSE, _ADJ);
      } else {
        layer_off(_LWR);
        update_tri_layer(_LWR, _RSE, _ADJ);
      }
      return false;
      break;
    case RSE:
      if (record->event.pressed) {
        layer_on(_RSE);
        update_tri_layer(_LWR, _RSE, _ADJ);
      } else {
        layer_off(_RSE);
        update_tri_layer(_LWR, _RSE, _ADJ);
      }
      return false;
      break;
    case ADJ:
      if (record->event.pressed) {
        layer_on(_ADJ);
      } else {
        layer_off(_ADJ);
      }
      return false;
      break;
  }
  return true;
}
